    bst.insert(30)
    bst.insert(70)
    bst.insert(20)
    bst.insert(40)
    bst.insert(60)
    bst.insert(80)
    
    print("Inorder traversal:", bst.inorder_traversal())
    
    bst.delete(20)
    print("After deleting 20:", bst.inorder_traversal())
    
    bst.delete(30)
    print("After deleting 30:", bst.inorder_traversal())
    
    bst.delete(50)
